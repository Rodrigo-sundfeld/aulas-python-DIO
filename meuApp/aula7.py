# Em Python metodo é chamado de definição
# Em um metodo temos sempre um retorno
def soma(a, b):
    return a + b


def subtracao(a, b):
    return a - b


def muliplicacao(a, b):
    return a * b


def divisao(a, b):
    return a / b


print(soma(60, 100))
print(subtracao(60, 100))
print(muliplicacao(10, 20))
print(divisao(30, 18))
